    focusMap = [ ...
        focusEntry("differential_input", "Differential input", "Differential signal at the two input ports."), ...
        focusEntry("common_mode_input", "Common-mode input", "Shared input signal to reject."), ...
        focusEntry("gain_setting_resistor", "Gain-setting resistor", "Resistor that sets instrumentation amplifier gain."), ...
        focusEntry("feedback_loop", "Feedback loop", "Output-to-inverting-input loop to highlight."), ...
