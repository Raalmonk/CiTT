function entry = focusEntry(id, label, description)
entry = struct();
entry.id = id;
entry.label = label;
entry.mode = "teach";
entry.description = description;
entry.targets = struct("id", id, "label", label, "target_type", "conceptual_path", ...
    "model_path", "", "style", "default", "reason", description);
entry.student_prompt = "";
end

