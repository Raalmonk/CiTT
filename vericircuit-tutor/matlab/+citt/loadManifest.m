function manifest = loadManifest(labId)
%LOADMANIFEST Load CiTT playground manifest from API or built-in fallback.

if nargin < 1 || strlength(string(labId)) == 0
    labId = "rc_antialias_adc";
end

apiUrl = "http://127.0.0.1:8000/matlab_playground/manifest";
try
    options = weboptions("Timeout", 2);
    manifest = webread(apiUrl, options);
    return
catch apiError
    warning("CiTT:ManifestFallback", ...
        "Backend manifest unavailable (%s). Loading built-in offline manifest.", apiError.message);
end

manifest = builtinManifest(string(labId));
end

function manifest = builtinManifest(labId)
manifest = struct();
manifest.product_name = "CiTT MATLAB/Simscape Playground";
manifest.version = "1.0.0";
manifest.positioning = "Graphical tutor layer for BME circuits and signal-conditioning labs.";
manifest.tabs = ["overview", "teach", "probe", "lab_delta"];
manifest.labs = [ ...
    struct("id", "rc_antialias_adc", ...
        "title", "RC anti-aliasing filter before ADC", ...
        "summary", "ECG-like signal, RC low-pass filtering, sampling, probes, and Lab Delta.", ...
        "bme_context", "Signal-conditioning before ADC sampling."), ...
    struct("id", "instrumentation_amplifier_feedback", ...
        "title", "Instrumentation amplifier feedback focus", ...
        "summary", "Differential/common-mode input, gain setting, feedback-loop highlight, and output probe.", ...
        "bme_context", "ECG instrumentation front-end teaching path.") ...
    ];
manifest.focus_map = builtinFocusMap(labId);
manifest.probe_plans = builtinProbePlans(labId);
manifest.notes = ["Offline fallback manifest", "Use API refresh when the FastAPI backend is running"];
end

